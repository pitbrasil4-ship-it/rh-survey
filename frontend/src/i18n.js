// ─── Helper de tradução ──────────────────────────────────────────────────────
export function t(lang, key, vars) {
  const dict = STRINGS[lang] || STRINGS.pt;
  let s = (key in dict) ? dict[key] : (key in STRINGS.pt ? STRINGS.pt[key] : key);
  if (vars) for (const k in vars) s = s.split("{" + k + "}").join(String(vars[k]));
  return s;
}

